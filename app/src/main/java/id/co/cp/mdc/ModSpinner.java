package id.co.cp.mdc;

public class ModSpinner {
	private String zid;
	private String zdesc;
	private String flag;

	public String getZid() {
		return zid;
	}

	public void setZid(String zid) {
		this.zid = zid;
	}

	public String getDesc() {
		return zdesc;
	}

	public void setDesc(String zdesc) {
		this.zdesc = zdesc;
	}

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}


}